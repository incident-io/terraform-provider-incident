// TestAlertRouteV2RoundTrip is a light round-trip of the v2 mapping. Its main
// job (beyond confirming v2 fields survive) is to assert that the v3-only
// fields on the shared merged model come back null in v2 mode, so v2 state
// keeps its shape and IsV3Mode stays false.
func TestAlertRouteV2RoundTrip(t *testing.T) {
	api := client.AlertRouteV2{
		Id:        "01ABC",
		Name:      "route",
		Enabled:   true,
		IsPrivate: false,
		Version:   2,
		AlertSources: []client.AlertRouteAlertSourceV2{
			{AlertSourceId: "01SRC", ConditionGroups: []client.ConditionGroupV2{}},
		},
		ChannelConfig:   []client.AlertRouteChannelConfigV2{},
		ConditionGroups: []client.ConditionGroupV2{},
		Expressions:     []client.ExpressionV2{},
		EscalationConfig: client.AlertRouteEscalationConfigV2{
			AutoCancelEscalations: true,
			EscalationTargets:     []client.AlertRouteEscalationTargetV2{},
		},
		IncidentConfig: client.AlertRouteIncidentConfigV2{
			AutoDeclineEnabled:      false,
			AutoRelateGroupedAlerts: true,
			DeferTimeSeconds:        0,
			Enabled:                 true,
			GroupingKeys:            []client.GroupingKeyV2{{Reference: "alert.title"}},
			GroupingWindowSeconds:   300,
		},
		IncidentTemplate: client.AlertRouteIncidentTemplateV2{
			Name: client.AlertRouteAutoGeneratedTemplateBindingV2{Autogenerated: true},
		},
	}

	model := AlertRouteResourceModel{}.FromAPIV2(api)

	// Mode: no grouping_config, so this is a v2-mode model.
	if model.IsV3Mode() {
		t.Error("expected IsV3Mode to be false for a v2 route")
	}

	// v2 fields survive.
	if got := model.Name.ValueString(); got != "route" {
		t.Errorf("name: got %q", got)
	}
	if got := model.IncidentConfig.GroupingWindowSeconds.ValueInt64(); got != 300 {
		t.Errorf("grouping_window_seconds: got %d", got)
	}
	if !model.IncidentConfig.AutoRelateGroupedAlerts.ValueBool() {
		t.Error("auto_relate_grouped_alerts should be true")
	}
	if got := len(model.IncidentConfig.GroupingKeys); got != 1 {
		t.Errorf("grouping_keys: got %d", got)
	}
	if !model.EscalationConfig.AutoCancelEscalations.ValueBool() {
		t.Error("auto_cancel_escalations should be true")
	}
	if model.IncidentTemplate == nil || !model.IncidentTemplate.Name.Autogenerated.ValueBool() {
		t.Error("incident_template.name should be autogenerated")
	}

	// v3-only fields must be null/nil in v2 mode.
	if !model.EscalationConfig.WhenAlertJoinsGroup.IsNull() {
		t.Error("escalation_config.when_alert_joins_group should be null in v2 mode")
	}
	if model.IncidentConfig.Template != nil {
		t.Error("incident_config.template should be nil in v2 mode")
	}
	if model.GroupingConfig != nil {
		t.Error("grouping_config should be nil in v2 mode")
	}
	if model.MessageConfig != nil {
		t.Error("message_config should be nil in v2 mode")
	}

	// Round-trip back to a payload and confirm key v2 fields are carried.
	payload := model.ToCreatePayloadV2()
	if payload.IncidentConfig.GroupingWindowSeconds != 300 {
		t.Errorf("payload grouping_window_seconds: got %d", payload.IncidentConfig.GroupingWindowSeconds)
	}
	if !payload.EscalationConfig.AutoCancelEscalations {
		t.Error("payload auto_cancel_escalations should be true")
	}
}

// Helpers for escalation-target tests: minimal API route and binding builders.

func alertRouteV2WithTargets(targets []client.AlertRouteEscalationTargetV2) client.AlertRouteV2 {
	return client.AlertRouteV2{
		Id:              "01ABC",
		Name:            "route",
		Enabled:         true,
		Version:         2,
		AlertSources:    []client.AlertRouteAlertSourceV2{},
		ChannelConfig:   []client.AlertRouteChannelConfigV2{},
		ConditionGroups: []client.ConditionGroupV2{},
		Expressions:     []client.ExpressionV2{},
		EscalationConfig: client.AlertRouteEscalationConfigV2{
			AutoCancelEscalations: true,
			EscalationTargets:     targets,
		},
		IncidentConfig: client.AlertRouteIncidentConfigV2{
			Enabled:               true,
			GroupingKeys:          []client.GroupingKeyV2{},
			GroupingWindowSeconds: 300,
		},
		IncidentTemplate: client.AlertRouteIncidentTemplateV2{
			Name: client.AlertRouteAutoGeneratedTemplateBindingV2{Autogenerated: true},
		},
	}
}

func apiArrayBindingV2(value client.EngineParamBindingValueV2) *client.EngineParamBindingV2 {
	return &client.EngineParamBindingV2{
		ArrayValue: &[]client.EngineParamBindingValueV2{value},
	}
}

func planArrayBinding(value client.EngineParamBindingValueV2) *IncidentEngineParamBinding {
	binding := IncidentEngineParamBinding{}.FromAPI(*apiArrayBindingV2(value))
	return &binding
}

func planWithEscalationTargets(targets []AlertRouteEscalationTargetModel) *AlertRouteResourceModel {
	return &AlertRouteResourceModel{
		EscalationConfig: &AlertRouteEscalationConfigModel{
			EscalationTargets: targets,
		},
	}
}

// TestAlertRouteV2EscalationTargetUsersEcho covers the API behaviour where the
// escalation_paths binding of a target is echoed back in its users field even
// though the request never set users (issue #390). Storing the echo makes the
// applied state disagree with the plan and the framework fails the apply, so
// the conversion must drop it — without touching targets whose users the user
// actually configured, users-only targets, or out-of-band remote state.
func TestAlertRouteV2EscalationTargetUsersEcho(t *testing.T) {
	pathLiteral := client.EngineParamBindingValueV2{Literal: lo.ToPtr("01PATH")}
	otherPathLiteral := client.EngineParamBindingValueV2{Literal: lo.ToPtr("01OTHERPATH")}
	pathReference := client.EngineParamBindingValueV2{Reference: lo.ToPtr(`expressions["xxx"]`)}
	userLiteral := client.EngineParamBindingValueV2{Literal: lo.ToPtr("01USER")}

	tests := []struct {
		name      string
		api       []client.AlertRouteEscalationTargetV2
		plan      *AlertRouteResourceModel
		wantUsers []bool // per target, in API order: users expected non-nil?
	}{
		{
			name: "echoed users dropped (literal)",
			api: []client.AlertRouteEscalationTargetV2{
				{EscalationPaths: apiArrayBindingV2(pathLiteral), Users: apiArrayBindingV2(pathLiteral)},
			},
			plan: planWithEscalationTargets([]AlertRouteEscalationTargetModel{
				{EscalationPaths: planArrayBinding(pathLiteral)},
			}),
			wantUsers: []bool{false},
		},
		{
			name: "echoed users dropped (reference)",
			api: []client.AlertRouteEscalationTargetV2{
				{EscalationPaths: apiArrayBindingV2(pathReference), Users: apiArrayBindingV2(pathReference)},
			},
			plan: planWithEscalationTargets([]AlertRouteEscalationTargetModel{
				{EscalationPaths: planArrayBinding(pathReference)},
			}),
			wantUsers: []bool{false},
		},
		{
			name: "users-only target kept alongside echoed paths target",
			api: []client.AlertRouteEscalationTargetV2{
				{Users: apiArrayBindingV2(userLiteral)},
				{EscalationPaths: apiArrayBindingV2(pathLiteral), Users: apiArrayBindingV2(pathLiteral)},
			},
			plan: planWithEscalationTargets([]AlertRouteEscalationTargetModel{
				{Users: planArrayBinding(userLiteral)},
				{EscalationPaths: planArrayBinding(pathLiteral)},
			}),
			wantUsers: []bool{true, false},
		},
		{
			name: "target with both fields configured keeps users",
			api: []client.AlertRouteEscalationTargetV2{
				{EscalationPaths: apiArrayBindingV2(pathLiteral), Users: apiArrayBindingV2(userLiteral)},
			},
			plan: planWithEscalationTargets([]AlertRouteEscalationTargetModel{
				{EscalationPaths: planArrayBinding(pathLiteral), Users: planArrayBinding(userLiteral)},
			}),
			wantUsers: []bool{true},
		},
		{
			name: "nil plan (import) passes API through",
			api: []client.AlertRouteEscalationTargetV2{
				{EscalationPaths: apiArrayBindingV2(pathLiteral), Users: apiArrayBindingV2(pathLiteral)},
			},
			plan:      nil,
			wantUsers: []bool{true},
		},
		{
			name: "unmatched target keeps users so drift stays visible",
			api: []client.AlertRouteEscalationTargetV2{
				{EscalationPaths: apiArrayBindingV2(pathLiteral), Users: apiArrayBindingV2(pathLiteral)},
			},
			plan: planWithEscalationTargets([]AlertRouteEscalationTargetModel{
				{EscalationPaths: planArrayBinding(otherPathLiteral)},
			}),
			wantUsers: []bool{true},
		},
	}

	for _, tc := range tests {
		t.Run(tc.name, func(t *testing.T) {
			api := alertRouteV2WithTargets(tc.api)

			var model AlertRouteResourceModel
			if tc.plan != nil {
				model = AlertRouteResourceModel{}.FromAPIV2WithPlan(api, tc.plan)
			} else {
				model = AlertRouteResourceModel{}.FromAPIV2(api)
			}

			targets := model.EscalationConfig.EscalationTargets
			if len(targets) != len(tc.api) {
				t.Fatalf("targets: got %d, want %d", len(targets), len(tc.api))
			}

			for i, wantUsers := range tc.wantUsers {
				if gotUsers := targets[i].Users != nil; gotUsers != wantUsers {
					t.Errorf("target %d users: got non-nil=%v, want non-nil=%v", i, gotUsers, wantUsers)
				}

				// escalation_paths must always survive untouched.
				wantPaths := tc.api[i].EscalationPaths != nil
				if gotPaths := targets[i].EscalationPaths != nil; gotPaths != wantPaths {
					t.Errorf("target %d escalation_paths: got non-nil=%v, want non-nil=%v", i, gotPaths, wantPaths)
				}
			}
		})
	}
}
